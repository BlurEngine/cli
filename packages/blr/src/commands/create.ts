type CreateCommandOptions = {
    namespace?: string;
    packageManager?: string;
    behaviorPack?: boolean;
    resourcePack?: boolean;
    language?: string;
    scripts?: boolean;
    bebe?: boolean;
    yes?: boolean;
    force?: boolean;
    install?: boolean;
};

